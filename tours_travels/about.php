    2).       Travel agencies often receive commissions and other benefits and incentives from providers or may charge a fee to the end users.[1] Hotel owners and tour operators typically pay a higher commission rate to travel agencies, whereas airlines typically pay a low commission.[2] The customer is normally not made aware of how much the travel agent is earning in commissions and other benefits.[3] A 2016 survey of 1,193 travel agents in the United States found that on average 78% of their revenue was from commissions and 22% was generated from fees.
